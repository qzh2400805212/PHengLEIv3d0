void ChemicalReactions::CreateChemicalReactionModel(vector<string> &namelist, ChemicalReactions *pNewModel)
{
    if (this->nSpecies <= 0 || this->nReaction <= 0)
        return;
    int nTargetSpecies = static_cast<int>(namelist.size());
    if (nTargetSpecies <= 0)
        return;
    //Record the new index of the source species in the new model.
    int flag[100], reactionFlag[70];
    for (int s = 0; s < nSpecies; ++ s)
    {
        flag[s] = SearchSpeciesInNameList(speciesName[s], namelist);
    }

    //Count the number of new reactions.
    int nTargetReactions = 0;
    bool rflag = true;
    for (int r = 0; r < this->nReaction; ++ r)
    {
        rflag = true;
        reactionFlag[r] = 0;
        for (int s = 0; s < nSpecies; ++ s)
        {
            if (stoichForward[r][s] > 0)
            {
                if (flag[s] == -1)
                {
                    rflag = false;
                    break;
                }
            }
            if (stoichBackward[r][s] > 0)
            {
                if (flag[s] == -1)
                {
                    rflag = false;
                    break;
                }
            }
        }
        if (rflag)
        {
            ++ nTargetReactions;
            reactionFlag[r] = 1; //1 indicates the current reaction is kept.
        }
    }

    //Generate the new reaction model.
    pNewModel->SetChemicalReactionType(this->nReactionType);
    pNewModel->SetChemicalModelInfo(nTargetSpecies, nTargetReactions);
    string *targetName = pNewModel->GetSpeciesNames();
    for (int s = 0; s < nTargetSpecies; ++ s)
    {
        targetName[s] = namelist[s];
    }
    pNewModel->Initialization();
    int **targetReactionType = pNewModel->GetReactionType();
    int **targetStoichForward = pNewModel->GetForwardStoichiometricCoefficient();
    int **targetStoichBackward = pNewModel->GetBackwardStoichiometricCoefficient();
    RDouble **targetCollisionCoef = pNewModel->GetThirdBodyCollisionCoefficient();
    RDouble **targetReactionData = pNewModel->GetReactionRateCurveFittingData();
    int nr = -1, ns = -1;
    for (int r = 0; r < this->nReaction; ++ r)
    {
        if (reactionFlag[r] != 1)
        {
            continue;
        }
        ++ nr;
        for (int s = 0; s < nSpecies; ++ s)
        {
            ns = flag[s];
            if (ns != -1)
            {
                targetReactionType[nr][0] = this->reactionType[r][0];
                targetReactionType[nr][1] = this->reactionType[r][1];

                targetStoichForward[nr][ns] = this->stoichForward[r][s];
                targetStoichBackward[nr][ns] = this->stoichBackward[r][s];
                targetCollisionCoef[nr][ns] = this->collisionCoef[r][s];
            }
        }
        for (int s = 0; s < 6; ++ s)
        {
            targetReactionData[nr][s] = this->reactionData[r][s];
        }
    }
}

int SearchSpeciesInNameList(string strName, vector<string> &namelist)
{
    int num = static_cast<int>(namelist.size());
    for (int s = 0; s < num; ++ s)
    {
        if (namelist[s] == strName)
            return s;
    }
    return -1;
}

