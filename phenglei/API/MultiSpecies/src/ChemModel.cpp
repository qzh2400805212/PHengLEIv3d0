void ChemkinFitData::CreateChemkinFitData(vector<string> &namelist, ChemkinFitData *pNewData)
{
    if (this->nSpecies <= 0)
        return;
    int nTargetSpecies = static_cast<int>(namelist.size());
    if (nTargetSpecies <= 0)
        return;

    //Record the new index of the source species in the new model.
    int flag[100];
    for (int s = 0; s < this->nSpecies; ++ s)
    {
        flag[s] = SearchSpeciesInNameList(speciesName[s], namelist);
    }

    //Generate the new curve fitting data.
    pNewData->SetInterpolationInfo(nTargetSpecies, this->nInterval, this->nCoefNum);
    string *targetName = pNewData->GetSpeciesNames();
    for (int s = 0; s < nTargetSpecies; ++ s)
    {
        targetName[s] = namelist[s];
    }

    RDouble *targetTemp = pNewData->GetTemperatureRange();
    for (int s = 0; s < 6; ++ s)
    {
        targetTemp[s] = this->temperatureRange[s];
    }
    RDouble **targetEnthalpyFitData = pNewData->GetEnthalpyFitData();
    RDouble ***targetFitData = pNewData->GetCurveFittingData();
    RDouble *targetTempStart = pNewData->GetStartTemperature();
    RDouble ***targetFitDataT2 = pNewData->GetCurveFittingDataT2();

    int nIndex = -1;
    for (int s = 0; s < this->nSpecies; ++ s)
    {
        nIndex = flag[s];
        if (nIndex == -1)
            continue;
        for (int i = 0; i < this->nInterval; ++ i)
        {
            for (int j = 0; j < this->nCoefNum; ++ j)
            {
                targetFitData[nIndex][i][j] = this->curveFittingData[s][i][j];
            }
        }

        for (int j = 0; j < this->nCoefNum; ++ j)
        {
            targetEnthalpyFitData[nIndex][j] = this->enthalpyFitData[s][j];
        }

        targetTempStart[nIndex] = this->temperatureStart[s];
        for (int i = 0; i < this->nInterval + 1; ++ i)
        {
            for (int j = 0; j < this->nCoefNum; ++ j)
            {
                targetFitDataT2[nIndex][i][j] = this->curveFittingDataT2[s][i][j];
            }
        }
    }
}

