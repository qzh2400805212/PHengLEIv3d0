void TransportFitData::CreateTransportFitData(vector<string> &namelist, TransportFitData *pNewData)
{
    if (this->nSpecies <= 0)
        return;
    int nTargetSpecies = static_cast<int>(namelist.size());
    if (nTargetSpecies <= 0)
        return;

    //Record the new index of the source species in the new model.
    int flag[100];
    for (int s = 0; s < this->nSpecies; ++ s)
    {
        flag[s] = SearchSpeciesInNameList(speciesName[s], namelist);
    }

    //Generate the new curve fitting data.
    pNewData->SetSpeciesNumber(nTargetSpecies);
    string *targetName = pNewData->GetSpeciesNames();
    for (int s = 0; s < nTargetSpecies; ++ s)
    {
        targetName[s] = namelist[s];
    }
    RDouble **targetViscosityFitData = pNewData->GetViscosityCurveFittingData();
    RDouble **targetCollisionAreaFitData = pNewData->GetCollisionAreaCurveFittingData();

    int nIndex = -1;
    for (int s = 0; s < this->nSpecies; ++ s)
    {
        nIndex = flag[s];
        if (nIndex == -1)
            continue;
        for (int i = 0; i < 5; ++ i)
        {
            targetViscosityFitData[nIndex][i] = this->viscosityFitData[s][i];
        }
        for (int i = 0; i < 4; ++ i)
        {
            targetCollisionAreaFitData[nIndex][i] = this->collisionAreaFitData[s][i];
        }
    }
}

