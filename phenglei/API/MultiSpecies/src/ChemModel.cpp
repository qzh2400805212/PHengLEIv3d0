void AtmosphereData::CreateAtmosphereData(vector<string> &namelist, AtmosphereData *pNewData)
{
    if (this->nSpecies <= 0)
        return;
    int nTargetSpecies = static_cast<int>(namelist.size());
    if (nTargetSpecies <= 0)
        return;

    //Record the new index of the source species in the new model.
    int flag[100];
    for (int s = 0; s < this->nSpecies; ++ s)
    {
        flag[s] = SearchSpeciesInNameList(speciesName[s], namelist);
    }

    //Generate the new data.
    pNewData->SetSpeciesNumber(nTargetSpecies);
    string *targetName = pNewData->GetSpeciesNames();
    for (int s = 0; s < nTargetSpecies; ++ s)
    {
        targetName[s] = namelist[s];
    }
    int *targetType = pNewData->GetSpeciesType();
    int *targetCharge = pNewData->GetSpeciesCharge();
    int *targetG0 = pNewData->GetSpeciesDegeneracy0();
    int *targetG1 = pNewData->GetSpeciesDegeneracy1();
    RDouble *targetMass = pNewData->GetSpeciesMolecularWeight();
    RDouble *targetT = pNewData->GetSpeciesTemperature();
    VibrationModeData *targetTv = pNewData->GetSpeciesVibrationalModeData();
    RDouble *targetTe = pNewData->GetSpeciesElectronTemperature();
    RDouble *targetH0 = pNewData->GetSpeciesFormationEnthalpy();
    RDouble *targetSigma = pNewData->GetSpeciesCollisionArea();
    //Reconstruct.
    int nIndex = -1;
    for (int s = 0; s < this->nSpecies; ++ s)
    {
        nIndex = flag[s];
        if (nIndex == -1)
            continue;
        targetType[nIndex] = this->nSpeciesType[s];
        targetCharge[nIndex] = this->nSpeciesCharge[s];
        targetG0[nIndex] = this->nSpeciesG0[s];
        targetG1[nIndex] = this->nSpeciesG1[s];

        targetMass[nIndex] = this->speciesMass[s];
        targetT[nIndex] = this->speciesTtr[s];
        targetTe[nIndex] = this->speciesTes[s];
        targetSigma[nIndex] = this->speciesSigma[s];
        targetH0[nIndex] = this->speciesEnthalpy[s];
        targetTv[nIndex] = this->speciesVibrationalModeData[s];
    }
}
