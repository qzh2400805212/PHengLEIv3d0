ChemicalReactions::ChemicalReactions()
{
    nReactionType = 0; //nType = 0 indicates the Gupta Model, nType = 1 indicates the Park Model, and nType = 2 indicates the Dunn-Kang Model.
    nSpecies = 0;
    nReaction = 0;
    speciesName = NULL;
    stoichForward = NULL;
    stoichBackward = NULL;
    collisionCoef = NULL;
    reactionData = NULL;
    reactionType = NULL; 
}

ChemicalReactions::ChemicalReactions(int ns, int nr)
{
    if (ns <= 0 || nr <= 0)
    {
        nReactionType = 0;
        nSpecies = 0;
        nReaction = 0;
        speciesName = NULL;
        stoichForward = NULL;
        stoichBackward = NULL;
        collisionCoef = NULL;
        reactionData = NULL;
        reactionType = NULL; 
    }
    else
    {
        nSpecies = ns;
        nReaction = nr;
        nReactionType = 0;
        AllocateMemories();
    }
}

ChemicalReactions::~ChemicalReactions()
{
    DeAllocateMemories();
}

void ChemicalReactions::AllocateMemories()
{
    if (this->nSpecies <= 0 || this->nReaction <= 0)
        return;
    speciesName = new string[nSpecies];
    stoichForward = NewPointer2 <int> (this->nReaction, this->nSpecies);
    stoichBackward = NewPointer2 <int> (this->nReaction, this->nSpecies);
    collisionCoef = NewPointer2 <RDouble> (this->nReaction, this->nSpecies);
    reactionData = NewPointer2 <RDouble> (this->nReaction, 6);
    reactionType = NewPointer2 <int> (this->nReaction, 2);
}

void ChemicalReactions::DeAllocateMemories()
{
    if (this->nSpecies <= 0 || this->nReaction <= 0)
        return;
    // Remove the allocated memories.
    delete [] speciesName;
    DelPointer2(stoichForward);
    DelPointer2(stoichBackward);
    DelPointer2(collisionCoef);
    DelPointer2(reactionData);
    DelPointer2(reactionType);

    nSpecies = 0;
    nReaction = 0;
    speciesName = NULL;
    stoichForward = NULL;
    stoichBackward = NULL;
    collisionCoef = NULL;
    reactionData = NULL;
    reactionType = NULL;
}

void ChemicalReactions::SetChemicalModelInfo(int ns, int nr)
{
    if (ns <= 0 || nr <= 0)
        return;
    if (this->nSpecies == ns && this->nReaction == nr)
        return;
    DeAllocateMemories();

    this->nSpecies = ns;
    this->nReaction = nr;
    AllocateMemories();
}

