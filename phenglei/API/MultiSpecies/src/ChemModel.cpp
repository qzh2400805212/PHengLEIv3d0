{
    if (this->nSpecies <= 0)
        return;
    delete [] speciesName;
    DelPointer2(viscosityFitData);
    DelPointer2(collisionAreaFitData);
    nSpecies = 0;
    speciesName = NULL;
    viscosityFitData = NULL;
    collisionAreaFitData = NULL;
}

void TransportFitData::SetSpeciesNumber(int ns)
{
    if (ns <= 0 || ns == this->nSpecies)
        return;
    DeAllocateMemories();

    this->nSpecies = ns;
    AllocateMemories();
}

