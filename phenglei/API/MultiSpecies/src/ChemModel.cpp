void ChemkinFitData::AllocateMemories()
{
    if (nSpecies > 0 && nInterval > 0 && nCoefNum > 0)
    {
        speciesName = new string [nSpecies];
        temperatureRange = new RDouble[nInterval + 1];
        curveFittingData = NewPointer3 <RDouble> (this->nSpecies, nInterval, nCoefNum);
        enthalpyFitData = NewPointer2 <RDouble> (this->nSpecies, nCoefNum);

        temperatureStart = new RDouble [nSpecies];
        curveFittingDataT2 = NewPointer3 <RDouble> (this->nSpecies, nInterval + 1, nCoefNum);
    }
}

void ChemkinFitData::DeAllocateMemories()
{
    if (nSpecies > 0 && nInterval > 0 && nCoefNum > 0)
    {
        delete [] speciesName;
        delete [] temperatureRange;
        delete [] temperatureStart;
        DelPointer3(curveFittingData);
        DelPointer3(curveFittingDataT2);
        DelPointer2(enthalpyFitData);
        nSpecies = 0;//number of species;
        nInterval = 0;
        nCoefNum = 0;
        speciesName = NULL;
        curveFittingData = NULL;
        temperatureRange = NULL;
        temperatureStart = NULL;
        curveFittingDataT2 = NULL;
    }
}

void ChemkinFitData::SetInterpolationInfo(int ns, int m/* = 5*/, int n/* = 6*/)
{
    if (ns <= 0 || m <= 0 || n <= 0)
        return;
    if (ns == this->nSpecies && m == this->nInterval && n == this->nCoefNum)
        return;
    DeAllocateMemories();

    this->nSpecies = ns;
    this->nInterval = m;
    this->nCoefNum  = n;
    AllocateMemories();
}

